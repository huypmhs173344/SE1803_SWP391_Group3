package model;

import java.util.Date;

/**
 *
 * @author nitro5
 */
public class Order {
    int user_id;
    int total_money;
    int status;
    Date date;
    String note;
    int isPay;

    public Order(int user_id, int total_money, int status, Date date, String note, int isPay) {
        this.user_id = user_id;
        this.total_money = total_money;
        this.status = status;
        this.date = date;
        this.note = note;
        this.isPay = isPay;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getTotal_money() {
        return total_money;
    }

    public void setTotal_money(int total_money) {
        this.total_money = total_money;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public int getIsPay() {
        return isPay;
    }

    public void setIsPay(int isPay) {
        this.isPay = isPay;
    }
    
    
}
